#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0x8E,0x77,0x68,0x66,0x2D,0x5D,0x0C,0x42,0x1D,0xEC,0x63,0x6D,
		0xD0,0xF8,0xE6,0xC9,0x1D,0xE4,0x6B,0x04,0x57,0x4E,0xF5,0xEA,
		0x1C,0x43,0x88,0x67,0x79,0xAC,0x97,0x9E,0x93,0xAC,0x21,0x3E,
		0xB3,0x72,0x32,0x4C,0x41,0x38,0x36,0x50,0x26,0xFA,0xD9,0x2A,
		0x44,0xE5,0xD6,0x73,0x17,0xB6,0x45,0xE5,0x29,0xBC,0x91,0xCA,
		0xB4,0x1E,0x00,0x03,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
